# Exercício 3 - Calculadora de Volume
# Calcula o volume de uma caixa retangular

# Dimensões da caixa (em cm)
comprimento = 12
largura = 14
altura = 20

# Calcula o volume (comprimento × largura × altura)
volume = comprimento * largura * altura

# Exibe o resultado
print(f"Volume da caixa retangular:")
print(f"Comprimento: {comprimento} cm")
print(f"Largura: {largura} cm")
print(f"Altura: {altura} cm")
print(f"Volume: {volume} cm³")
